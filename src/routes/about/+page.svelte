<h1 class="title">About</h1>

<p>
  In collaboration with the MTA, CityTech and Eco Rising are analyzing Subway
  temperatures across New York City MTA stations to observe patterns and analyze
  the <a href="https://www.epa.gov/heatislands">urban heat island's effect</a> on
  underground subway stations. CityTech students are collecting temperatures at both
  the street level and platform level at these subway stations and through an analysis
  of temperatures collected, we will build an interactive dashboard that allows people
  to access generalized weather information through a geospatial web component.
</p>
<p>
  We are also working on creating a two-tiered forecast model that uses the
  temperatures collected to train a machine learning model that takes in daily
  temperatures and dates to predict street level temperatures and predict the
  temperatures inside of subway stations given the aforementioned analysis of
  historical subway records.
</p>

<h2>Project Description</h2>
<p>
  New York City faces significant challenges due to the accelerating impacts of
  climate change. The most affected infrastructures are underground
  transportation systems, which are particularly vulnerable to the intensifying
  frequency and duration of heat waves. The fluctuation in temperature within
  the subway system during these extreme weather events poses serious health and
  safety risks not only to daily commuters but also transit employees working in
  these environments. Despite the potential dangers, the extent and dynamics of
  temperature changes within the subway remain insufficiently understood. To
  address this knowledge gap, this study conducts a comprehensive investigation
  into temperature variability across multiple subway stations in NYC, aiming to
  inform future mitigation strategies and urban planning.
</p>

